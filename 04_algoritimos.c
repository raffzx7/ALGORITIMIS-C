#include <stdio.h>

int main() {
    int n1, n2;

    printf("insira um numero: ");
    scanf("%d", &n1);

    printf("insira outro numero:");
    scanf("%d", &n2);

    if (n1 > n2) {
        printf("o maior numero é: %d\n", n1);
    } else
	if (n2 > n1) {
        printf("o maior numero é: %d\n", n2);
    }
	else {
        printf("os dois numeros são iguais.\n");
    }

    return 0;
}
